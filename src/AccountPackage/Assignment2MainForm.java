    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
        String name = TxtName.getText();
        String ID = TxtID.getText();
        String Balance = TxtBalance.getText();
        
        if(TxtName.getText().equals("") || TxtID.getText().length() == 0 || TxtBalance.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Enter Account Information");
        }
        else{
            Account acc = new Account(name,Integer.parseInt(ID), Double.parseDouble(Balance));
            if(bank.updateAccount(acc)){
            JOptionPane.showMessageDialog(this, "Account Updated");
            TxtOutput.setText(bank.toDisplayFormat());
        }
        }
        
        
        
        
    }//GEN-LAST:event_BtnUpdateActionPerformed

    private void BtnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoveActionPerformed
        String name = TxtName.getText();
        String ID = TxtID.getText();
        String Balance = TxtBalance.getText();
        
        if(TxtID.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Enter Account Information");
        }
        else{
            Account acc = new Account(name,Integer.parseInt(ID), Double.parseDouble(Balance));
        if(bank.removeAccount(acc)){
            JOptionPane.showMessageDialog(this, "Account Removed");
            TxtOutput.setText(bank.toDisplayFormat());
        }
        }
    }//GEN-LAST:event_BtnRemoveActionPerformed

    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
      String save = bank.toArchiveFormat();
      List<String> file = new ArrayList();
      file.add(save);
      String filename = TxtSave.getText();
      FileUtils.writeListToFile(file, filename);
      JOptionPane.showMessageDialog(this, "File Saved");
    }//GEN-LAST:event_BtnSaveActionPerformed

    private void display(List<Account> account) {
        for (Formatable temp : account) {
            TxtOutput.append(temp.toDisplayFormat());
        }
        TxtOutput.append("Number of Accounts = " + account.size());
    }

