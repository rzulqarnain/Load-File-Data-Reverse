    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        
        String name = TxtName.getText();
        String ID = TxtID.getText();
        String Balance = TxtBalance.getText();
        
        
        if(TxtName.getText().equals("") || TxtID.getText().length() == 0 || TxtBalance.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Enter Account Information");
        }
        else{
            Account acc = new Account(name,Integer.parseInt(ID), Double.parseDouble(Balance));
            if(bank.addAccount(acc)){
            JOptionPane.showMessageDialog(this, "Account Added");
            TxtOutput.setText(bank.toDisplayFormat());
            }
        }
        
    }//GEN-LAST:event_BtnAddActionPerformed

